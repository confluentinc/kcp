// envConfig holds connection details loaded from the .env file generated by
// setup.sh, resolved for a specific scenario.
type envConfig struct {
	Kubeconfig        string // host kubeconfig for assertions
	KubeContext       string
	Namespace         string
	SourceBootstrap   string
	DestBootstrap     string
	RestProxyEndpoint string
	DestClusterID     string
	ClusterAPIKey     string
	ClusterAPISecret  string
	KCPPod            string // pod running the kcp binary
	KubePath          string // in-pod kubeconfig path

	// Per-scenario resources.
	Scenario        string
	ClusterLinkName string
	TopicName       string   // single-topic scenarios (producer/consumer target)
	TopicNames      []string // all mirror topics for the scenario (>=1)
	GatewayName     string
	FencedCR        string // in-pod fixture path
	SwitchoverCR    string // in-pod fixture path
}

func loadEnvConfig(t *testing.T, scenario string) envConfig {
	t.Helper()

	envFile := filepath.Join(testdataDir(), ".env")
	f, err := os.Open(envFile)
	require.NoError(t, err, "failed to open .env file — did you run 'make test-migration-setup'?")
	defer f.Close()

	vars := map[string]string{}
	scanner := bufio.NewScanner(f)
	for scanner.Scan() {
		line := strings.TrimSpace(scanner.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}
		parts := strings.SplitN(line, "=", 2)
		if len(parts) == 2 {
			vars[parts[0]] = parts[1]
		}
	}
	require.NoError(t, scanner.Err())

	// Sanity-check the scenario was provisioned by setup.sh.
	provisioned := strings.Split(vars["KCP_E2E_SCENARIOS"], ",")
	found := false
	for _, s := range provisioned {
		if s == scenario {
			found = true
			break
		}
	}
	require.True(t, found, "scenario %q not provisioned by setup.sh (KCP_E2E_SCENARIOS=%q)", scenario, vars["KCP_E2E_SCENARIOS"])

	// Per-scenario env-var prefix: lowercase-dash -> UPPERCASE_UNDERSCORE.
	prefix := "KCP_E2E_" + strings.ReplaceAll(strings.ToUpper(scenario), "-", "_") + "_"

	// TopicNames comes from the comma-separated <PREFIX>TOPIC_NAMES var when
	// present (multi-topic scenarios); otherwise it falls back to the single
	// TOPIC_NAME so every scenario exposes a non-empty list.
	topicName := vars[prefix+"TOPIC_NAME"]
	var topicNames []string
	if names := vars[prefix+"TOPIC_NAMES"]; names != "" {
		topicNames = strings.Split(names, ",")
	} else if topicName != "" {
		topicNames = []string{topicName}
	}

	return envConfig{
		Kubeconfig:        vars["KCP_E2E_KUBECONFIG"],
		KubeContext:       vars["KCP_E2E_KUBE_CONTEXT"],
		Namespace:         vars["KCP_E2E_NAMESPACE"],
		SourceBootstrap:   vars["KCP_E2E_SOURCE_BOOTSTRAP"],
		DestBootstrap:     vars["KCP_E2E_DEST_BOOTSTRAP"],
		RestProxyEndpoint: vars["KCP_E2E_REST_PROXY_ENDPOINT"],
		DestClusterID:     vars["KCP_E2E_DEST_CLUSTER_ID"],
		ClusterAPIKey:     vars["KCP_E2E_CLUSTER_API_KEY"],
		ClusterAPISecret:  vars["KCP_E2E_CLUSTER_API_SECRET"],
		KCPPod:            vars["KCP_E2E_KCP_POD"],
		KubePath:          vars["KCP_E2E_KUBE_PATH"],

		Scenario:        scenario,
		ClusterLinkName: vars[prefix+"CLUSTER_LINK_NAME"],
		TopicName:       topicName,
		TopicNames:      topicNames,
		GatewayName:     vars[prefix+"GATEWAY_NAME"],
		FencedCR:        vars[prefix+"FENCED_CR"],
		SwitchoverCR:    vars[prefix+"SWITCHOVER_CR"],
	}
}

